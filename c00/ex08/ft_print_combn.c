void    ft_print_combn(int n)
{
    char    a;
    a = 0;

    for(int i=0; i<n; i++)
    {
        while(a <= 10-n)
        {
            write(1, &a, 1);
            a++;
        }
    }
}

int main(void)
{
    ft_print_combn(2);
} 